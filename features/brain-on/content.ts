export function getBrainOnQuestionsForCategories(
  categoryIds: readonly BrainOnCategoryId[]
) {
  const categoryIdSet = new Set(categoryIds);

  return brainOnQuestions.filter((question) =>
    categoryIdSet.has(question.categoryId)
  );
}

export function getBrainOnQuestionById(questionId: string) {
  return brainOnQuestions.find((question) => question.id === questionId);
}

export function getBrainOnQuestionCounts(categoryId: BrainOnCategoryId) {
  return brainOnQuestions.filter(
    (question) => question.categoryId === categoryId
  ).length;
}
